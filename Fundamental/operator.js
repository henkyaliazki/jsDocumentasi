/* 
    *Dalam javascript terdapat operator seperti aritmatika dan operator 
    *untuk membantu dalam pengambilan keputusan

*/

/*
! Comparasion operator 
    *operator khusus yang membandingkan dua milai dan mengembalikan boolean
    *operator               fungsi
  !: ==             untuk membandingkan dua nilai tanpa tipe-datanya
  !: !=             nilai variabel yang tidak sama 
  !: ===            untuk membandingkan dua nilai dengan tipe-datanya
  !: !==            kebalikan dari nilai dan tipe-datanya
  !: <              lebih kecil dari pada nilai sebelumnya
  !: >              lebih besar dari nilai sebelumnya
  !: <=             lebih kecil sama dengan nilai
  !: >=             lebih besar sama dengan nilai          
*/

/* 
! Logical Operator 
    *Operator yang digunakan untuk masalah yang lebih kompleks
    *Operator              fungsi
    !: && (and)          menghasilkan nilai true apabila nilai keduanya true
    !: || (or)           menghasilkan nilai true apabila nilai salah satu true
    !: ! (not)           kebalikan dari nilai yang di bandingkan
*/

